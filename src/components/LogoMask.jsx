import { useId } from 'react'
import { gsap } from '../motion/gsap'
import { useFontsGsap } from '../motion/useFontsGsap'

/**
 * NeoBrick-style brand window: a photograph lives inside the actual SGS
 * letterforms. The photo pans vertically inside the letters while you
 * scroll (scrubbed, with inertia), so the mark reads as a window onto
 * the field. Crosshair rules stay SGS orange.
 */
